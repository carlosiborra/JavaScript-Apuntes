// ? MATHEMATICAL METHODS

console.log("MATHEMATICAL METHODS");
console.log("\n");

// ! Math.max()
console.log("Math.max()");
// * Math.max() es una función que nos permite obtener el valor máximo de una lista de números.
console.log(Math.max(1, 2, 3, 4, 5));
console.log("\n");

// ! Math.min()
console.log("Math.min()");
// * Math.min() es una función que nos permite obtener el valor mínimo de una lista de números.
console.log(Math.min(1, 2, 3, 4, 5));
console.log("\n");

// ! Math.random()
console.log("Math.random()");
// * Math.random() es una función que nos permite obtener un número aleatorio entre 0 y 1.
console.log(Math.random());
console.log("\n");

// ! Math.floor()
console.log("Math.floor()");
// * Math.floor() es una función que nos permite redondear un número hacia abajo.
console.log(Math.floor(4.9));
console.log("\n");

// ! Math.ceil()
console.log("Math.ceil()");
// * Math.ceil() es una función que nos permite redondear un número hacia arriba.
console.log(Math.ceil(4.1));
console.log("\n");

// ! Math.round()
console.log("Math.round()");
// * Math.round() es una función que nos permite redondear un número al entero más cercano.
console.log(Math.round(4.5)); // El número 4.5 se redondea al entero más cercano, que es 5.
console.log("\n");

// ! Math.trunc()
console.log("Math.trunc()");
// * Math.trunc() es una función que nos permite eliminar los decimales de un número.
console.log(Math.trunc(4.9)); // El número 4.9 se convierte en 4.
console.log("\n");

// ! Math.pow()
console.log("Math.pow()");
// * Math.pow() es una función que nos permite elevar un número a una potencia.
console.log(Math.pow(2, 3)); // El número 2 elevado a la potencia 3 es 8.
console.log("\n");

// ! Math.sqrt()
console.log("Math.sqrt()");
// * Math.sqrt() es una función que nos permite obtener la raíz cuadrada de un número.
console.log(Math.sqrt(9)); // La raíz cuadrada de 9 es 3.
console.log("\n");

// ! Math.abs()
console.log("Math.abs()");
// * Math.abs() es una función que nos permite obtener el valor absoluto de un número.
console.log(Math.abs(-4)); // El valor absoluto de -4 es 4.
console.log("\n");

// ! Math.sign()
console.log("Math.sign()");
// * Math.sign() es una función que nos permite obtener el signo de un número.
console.log(Math.sign(4)); // El signo de 4 es 1.
console.log(Math.sign(-4)); // El signo de -4 es -1.
console.log("\n");

// ! Math.sin()
console.log("Math.sin()");
// * Math.sin() es una función que nos permite obtener el seno de un ángulo.
console.log(Math.sin(0)); // El seno de 0 es 0.
console.log("\n");

// ! Math.cos()
console.log("Math.cos()");
// * Math.cos() es una función que nos permite obtener el coseno de un ángulo.
console.log(Math.cos(0)); // El coseno de 0 es 1.
console.log("\n");

// ! Math.tan()
console.log("Math.tan()");
// * Math.tan() es una función que nos permite obtener la tangente de un ángulo.
console.log(Math.tan(0)); // La tangente de 0 es 0.
console.log("\n");

// ! Math.asin()
console.log("Math.asin()");
// * Math.asin() es una función que nos permite obtener el ángulo cuyo seno es el número que le pasamos como parámetro.
console.log(Math.asin(0)); // El ángulo cuyo seno es 0 es 0.
console.log("\n");

// ! Math.acos()
console.log("Math.acos()");
// * Math.acos() es una función que nos permite obtener el ángulo cuyo coseno es el número que le pasamos como parámetro.
console.log(Math.acos(1)); // El ángulo cuyo coseno es 1 es 0.
console.log("\n");

// ! Math.atan()
console.log("Math.atan()");
// * Math.atan() es una función que nos permite obtener el ángulo cuya tangente es el número que le pasamos como parámetro.
console.log(Math.atan(0)); // El ángulo cuya tangente es 0 es 0.
console.log("\n");

// ! Math.log()
console.log("Math.log()");
// * Math.log() es una función que nos permite obtener el logaritmo natural de un número.
console.log(Math.log(1)); // El logaritmo natural de 1 es 0.
console.log("\n");

// ! Math.log10()
console.log("Math.log10()");
// * Math.log10() es una función que nos permite obtener el logaritmo base 10 de un número.
console.log(Math.log10(1)); // El logaritmo base 10 de 1 es 0.
console.log("\n");

// ! Math.log2()
console.log("Math.log2()");
// * Math.log2() es una función que nos permite obtener el logaritmo base 2 de un número.
console.log(Math.log2(1)); // El logaritmo base 2 de 1 es 0.
console.log("\n");

// ! Math.exp()
console.log("Math.exp()");
// * Math.exp() es una función que nos permite obtener e elevado a una potencia.
console.log(Math.exp(0)); // e elevado a la potencia 0 es 1.
console.log("\n");

// ! Math.hypot()
console.log("Math.hypot()");
// * Math.hypot() es una función que nos permite obtener la raíz cuadrada de la suma de los cuadrados de los números que le pasamos como parámetros.
console.log(Math.hypot(3, 4)); // La raíz cuadrada de la suma de los cuadrados de 3 y 4 es 5.
console.log("\n");
